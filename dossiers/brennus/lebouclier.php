      <p class="bold size5">Caractéristiques :</p>
      <p class ="size4 styleArial alignLeft">Le Bouclier de Brennus constitue le trophée des Champions de France depuis la 1ère finale du Championnat de France en 1892.<br>
     Bouclier damasquiné, 
        il présente en son centre les ""armes" de l'USFSA (Union des Sociétés Françaises des Sports Athètiques) qui à l'époque, gérait le rugby français 
        - sous forme de deux anneaux enlacés et offre comme devise "Ludus 
        pro patria" ("le jeu pour la patrie").<br>
     Le Bouclier de Brennus pèse 16,850kg pour une épaisseur de 3cm, une largeur de 76cm et une hauteur de 101cm.<br>
      Son origine est très controversée. Beaucoup, aujourd'hui encore, considèrent que son concepteur est Charles Brennus, figure emblématique du 
        rugby français et créateur du club du SCUF.</p>
