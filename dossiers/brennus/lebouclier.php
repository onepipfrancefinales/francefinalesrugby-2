      <p class="bold size5">Son créateur : Charles Brennus</p>
      <p class ="size4 styleArial alignLeft">Charles Brennus, pseudonyme 
        de Brennus Ambiorix Crosnier, est un médailleur, ciseleur et un 
        dirigeant sportif national français, né à Châteaudun 
        le 30 novembre 1859 et mort au Mans le 23 décembre 1943.<br>
      Président de la commission rugby de l''USFSA<br>
        président-fondateur du SCUF</br>
      Successivement joueur, 
        arbitre, dirigeant, il exer&ccedil;ait aussi la profession de ma&icirc;tre-graveur. 
        Un tel ""profil", comme disent maintenant les agents recruteurs, 
        ne peut faire douter un seul instant de la paternité du trophée.</br>
    Toutefois, si Charles 
        Brennus a sans nul doute ciselé le Bouclier, le mérite de 
        la conception revient probablement à re de la 1ère 
        finale et défenseur acharné des valeurs sportives et de 
        l''amateurisme de cette fin de siècle. Précisons aussi 
        que le &#147;ciseleur&#148; s''appelait de son vrai nom, Brennus Ambiorix 
        Crosnier.</p>
