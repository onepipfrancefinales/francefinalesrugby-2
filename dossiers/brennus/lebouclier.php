    <p class="bold size5">Le cérémonial :</p>
      <p class ="size4 styleArial alignLeft">Ce trophée 
        sera donc remis en 1892 au Racing Club de France puis dispara&icirc;tra 
        (mystère !) jusqu'en 1911. <br>
    Remis au Stade Toulousain 
        en 1912, il fut ignoré les deux années suivantes par Bayonne 
        et le SCUF pour revenir sur le pré en 1914 pour honorer Perpignan. 
</br>
      Ce n'est qu'à 
        partir de 1920 que le trophée est confié pour une année 
        entière au Champion. Le premier "gardien" fut le 
        Stadoceste Tarbais. </br>
     Ce n'est qu'à 
        la mort de Charles Brennus en 1943 que le bouclier fut remis officiellement 
        au vainqueur sur le stade à la fin de la finale. <br>
     Jusqu'en 1951, 
        le bouclier se trouvait sur le terrain durant la partie, gardé 
        par deux joueurs du SCUF, club de toujours de Charles Brennus. <br>
     En 1951, le capitaine 
        de l'US Carmaux, "Nénou" Pagès, vainqueur 
        de la finale, refusa de le recevoir après un combat très 
        âpre face à Tarbes. <br>
     L'année 
        suivante, le protocole fut changé. Le bouclier restait sur le terrain 
        durant la partie mais était remis au capitaine des champions par 
        le Président de la Fédération dans les tribunes. 
        <br>
     Le premier récipiendaire 
        fut Jean Prat lors de la victoire du FC Lourdes en 1952. Depuis cette 
        date, le protocole est immuable. <br>
     Notons que pour honorer 
        Brennus et le club qu'il créa, le bouclier est toujours remis 
        à l'équipe championne de France en présence 
        de deux joueurs de l'école de rugby du SCUF.<br></p>
