            <td>1901</td>

            <td> Le Stade Français est déclaré
              vainqueur par disqualification du SBUC malgré la victoire de
              ce dernier par 3 à 0. En effet, à la suite de la fusion
              des clubs du Stade Bordelais et du Bordeaux UC, trois joueurs de ce
              club n'ont pas les trois mois d'appartenance au club requis pour être
              qualifiés: Moyzes - Cazala et Lannes. L'USFSA annule le résultat
              et décide que la finale sera rejouée à Paris.
              Jugeant son honneur bafoué, le SBUC refuse le jugement et le
              Stade Français est déclaré champion de France
