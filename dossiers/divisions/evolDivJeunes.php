 <h2 class="bold styleArial">IV Compétitions Jeunes</h2>
 <br>
  <p>Les Competitions jeunes sont fonctions de l'age des participants.On distingue trois grandes catégories</p>
  
  <P class="bold">Cadets et U16 </p>
  <p>Une catégorie qui comprend les jeunes qui sont issus de l'école 
    de rugby (les cadets et U15 et u16 depuis 2019)<br>
    cette catégorie comprend les divisions Teulière ,Gaudermen et 
    Alamercery<br>
    <br>
    Gaudermen et Alamercery : compétitions réservés aux clubs 
    de l'élite.</p>
  <p>Teulière et U16 : compétitions réservés aux clubs de fédérales.<br>
  </p>
  <P class="bold">Juniors U18 ou Reichel </p>
  <p>l'autre catégorie, qui selon les périodes sera de 2 ou 3 ans 
    regroupera les joueurs avant leurs intégrations en seniors</p>

   
