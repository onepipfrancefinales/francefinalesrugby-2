


 

    
            
            <h2>Nouvelles régles de World rugby</h2>
<P class="alignLeft">World Rugby a adopté de nouvelles règles expérimentales pour rendre le jeu plus rapide, plus ouvert et plus dynamique. 
    La FFR et la LNR les appliquent depuis janvier 2025 dans les compétitions professionnelles :<p>

<p class="alignLeft">
La limite de 60 secondes pour les transformations, afin de s’aligner sur les pénalités et d’accélérer le rythme – <br>
La limite de 30 secondes de préparation pour les alignements en touche pour réduire les temps morts,<br>
L’utilisation du « jouer » ou « play on » pour les touches non contestées lorsque le lancer n’est pas droit,<br>
La protection du demi de mêlée pendant les mêlées, les rucks et les mauls.</p>


<p >
Ces nouvelles règles portant uniquement sur la fluidité du jeu. 
ces règles sont étendues à l’ensemble des compétitions officielles fédérales et régionales. 
Le déploiement sera effectif à partir du 8 février 2025 pour toutes les compétitions fédérales.</p>




<h2>Chronométrage</h2>
<h3>Tentative de transformation</h3>
<span class="bold">Règle 8.8.c  :</span> Le botteur doit botter en 60 secondes (temps de jeu) à compter du moment où l’essai a été marqué,
 même si le ballon tombe et doit être replacé.<br>
 <span class="bold">Sanction :</span> Le but est refusé.<br>
Le botteur peut dorénavant utiliser un autre ballon que celui qui était en jeu.
Le délai pour une tentative de transformation est identique à celui alloué à une tentative de but sur pénalité.

<h3>Formation d’un alignement</h3>
<span class="bold">Règle 18.12  : </span>Les équipes forment l’alignement dans un délai de 30 secondes 
après l’indication de la marque par un officiel de match.<br>
<span class="bold">Sanction : </span>Coup Franc (à 15 mètres de la ligne de touche)
Le délai de formation d’un alignement est identique à celui alloué à la formation d’une mêlée.

<h2>Protection du demi de mêlée</h2>
<h3>La mêlée</h3>
<span class="bold">Règle 19.30.a :</span> Une fois la mêlée commencée, le demi de mêlée de l’équipe qui n’est pas en possession du ballon doit se positionner 
près de la mêlée, sans qu’aucun de ses pieds ne dépasse la ligne médiane du tunnel.<br>
<span class="bold">Sanction : </span>Pénalité

<h3>Le ruck</h3>
<span class="bold">Règle 15.18 :</span> Un joueur qui fait ou qui a fait partie du ruck ne peut pas intervenir sur un adversaire
 qui se trouve près de ce ruck (à moins de 1 m) et qui tente d’en faire sortir le ballon.<br>
 <span class="bold">Sanction : </span>Pénalité

<h3>Le maul</h3>
<span class="bold">Règle 16.18 :</span> Un joueur qui fait ou qui faisait partie du maul ne peut pas intervenir sur un adversaire
 près de ce maul (à moins de 1 m) et qui tente d’en faire sortir le ballon.<br>
<span class="bold">Sanction : </span>Pénalité

<h2>Alignement</h2>
<h3>Le lancer n’est pas droit</h3>
<span class="bold">Règle 18.23.a  :</span> Le ballon doit être lancé droit le long de la ligne de remise en jeu.<br>
Sanction : Si le lancer est réalisé vers un joueur de l’alignement (pas droit) et que l’équipe 
qui n’effectue pas le lancer ne soulève pas un de ses joueurs pour lutter pour le gain du ballon, le jeu doit continuer.

         
