        (en gallois) <br>
        Mae hen wald fy nhadau yn annwyl i mi<br>
        O land of my father, the land of the free<br>
        Gwlad beirdd a chantorion, enwogion o fri<br>
        Of poets and singers, so soothing to me <br>
        Ei gwrol ryfelwyr, gwlad garwyr tra mad<br>
        Thy noble defenders were gallent and brave <br>
        Tros ryddid gollasant eu gwaed. <br>
        For freedom their hearts life they give. <br>
        --------------------------------------------------------------------<br>
        REFRAIN : Gwlad, Gwald. pleidiol wyf im gwlad<br>
        Wales! Wales ! home, sweet home, is Wales <br>
        Tra mor yn fur, i'r hoff bau<br>
        Till death be passed, my love shall last <br>
        O bydded i'r heniaith barhau. <br>
        My longing, my yearning for Wales. <br>
        --------------------------------------------------------------------<br>
        Hen Gymru fynyddig. paradwys y bardd<br>
        Thou haven of bards and birthplace of song<br>
        Pob dyffryn, pob clogwyn i'm golwg sydd hardd<br>
        The sons of thy mountains are valiant and strong<br>
        Trwy deimlad gwladgarol, mor swynol yw si <br>
        The voice of thy streamlets is soft to the ear<br>
        Ei nentydd, afonydd i mi !<br>
        Thy hills and thy valleys, how dear ! <br>
        --------------------------------------------------------------------<br>
        Os treisiodd y gelyn fy ngwlad tan ei droed<br>
        Tho slighted and scorned by the proud and the strong <br>
        Mae hen iaith y Cymry mor fyw ag erioed<br>
        The language of Cambria still charms us in song<br>
        Ni luddiwyd yr awen gan erchyll law brad<br>
        The muse survives. nor have envious tales <br>
        Na thelyn berseiniol fy ngwlad. <br>
        yet silenced the harps of dear Wales.<br>
