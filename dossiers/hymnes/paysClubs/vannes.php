    <td colspan="3" class="texte"> 
  
    <span class="gras">Ni, Breizhiz a galon, karomp hon gwir Vro !</span><br>
    Nous, Bretons de cœur, nous aimons notre vrai pays !<br>
    <span class="gras">Brudet eo an Arvor dre ar bed tro-dro.</span><br>
    Nous, Bretons de cœur, nous aimons notre vrai pays !<br>
    <span class="gras">Dispont kreiz ar brezel, hon tadoù ken mat</span>,<br>
    Sans peur au cœur de la guerre, nos ancêtres si bons<br>
    <span class="gras">À skuilhas eviti o gwad.</span><br>
    Versèrent leur sang pour elle.<br>
<br>



<span class="gras">Refrain :</span><br>
<span class="gras">O Breizh, ma Bro, me 'gar ma Bro.</span><br>
Ô Bretagne, mon pays, que j’aime mon pays<br>
<span class="gras">Tra ma vo mor 'vel mur 'n he zro.</span><br>
Tant que la mer sera comme un mur autour d’elle.<br>
<span class="gras">Ra vezo digabestr ma Bro !</span><br>
Sois libre, mon pays !<br>
<br>

<span class="gras">Breizh, douar ar Sent kozh, douar ar Varzhed,</span><br>
Bretagne, terre des vieux Saints, terre des Bardes,<br>
<span class="gras">N’eus bro all a garan kement 'barzh ar bed,</span><br>
Il n’est d’autre pays au monde que j’aime autant,<br>
<span class="gras">Pep menez, pep traonienn, d’am c’halon zo kaer,</span><br>
Chaque montagne, chaque vallée est chère dans mon cœur.<br>
<span class="gras">Enne kousk meur a Vreizhad taer !</span><br>
Bretagne, terre des vieux Saints, terre des Bardes,<br>
<br>

Refrain<br>

<br>
<span class="gras">Ar Vretoned 'zo tud kalet ha kreñv,</span><br>
Les Bretons sont des gens durs et forts,<br>
<span class="gras">N’eus pobl ken kalonek a zindan an neñv,</span><br>
Aucun peuple sous les cieux n’est aussi ardent.<br>
<span class="gras">Gwerz trist, son dudius a ziwan eno,</span><br>
Complaintes tristes ou chants plaisants s’éclosent en eux.<br>
<span class="gras">O ! pegen kaer ec’h out, ma Bro !</span><br>
Ô ! Combien tu es belle, ma patrie !<br>
<br>


Refrain<br>
<br>

<span class="gras">Mar d’eo bet trec’het Breizh er brezelioù bras,</span><br>
Si autrefois, Bretagne, tu as fléchi durant les guerres,<br>
<span class="gras">He yezh a zo bepred ken beo ha bizkoazh,</span><br>
Ta langue est restée vivante à jamais,<br>
<span class="gras">He c’halon birvidik a lamm c’hoazh 'n he c’hreiz</span><br>
Son cœur ardent tressaille encore pour elle.<br>
<span class="gras">Dihunet out bremañ, ma Breizh !</span><br>
Tu es réveillée maintenant ma Bretagne !<br>

